public class Change {
	//转义字符
	public static void main(String[] args){
		// 输出
		System.out.println("北京\t天津\t上海");
		System.out.println("红楼梦\n西游记\n水浒传");
		System.out.println("D:\\Java\\code");
		System.out.println("D:\\\\Java\\\\code");
		System.out.println("Andy:\"Just Do It\"");
		System.out.println("Andy:\'Just Do It\'");
		// 回车
		System.out.println("向阳而生\r不问前尘\n");
		System.out.println("向阳而生\r\n不问前尘");
	}
}